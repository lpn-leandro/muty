"use strict";
export class Album {
  constructor(
    albumArt,
    albumName,
    albumArtist,
    albumRate,
    albumMusics
  ) {
    this.albumArt = albumArt;
    this.albumName = albumName;
    this.albumArtist = albumArtist;
    this.albumRate = albumRate;
    this.albumMusics = albumMusics;
  }
}